function generateRandomLevel(difficulty: number): number[][] {
  const base: number[][] = Array(18).fill(0).map(() => Array(25).fill(0));

  // Bottom platforms
  base[16].fill(1);
  base[17].fill(1);

  // Add platforms and traps based on difficulty
  const trapDensity = Math.min(difficulty / 20, 0.7);

  for (let y = 12; y < 16; y++) {
    for (let x = 4; x < 21; x++) {
      if (Math.random() < 0.3) {
        if (Math.random() < trapDensity) {
          // Add traps
          const trapType = Math.floor(Math.random() * 4) + 2; // 2-5
          base[y][x] = trapType;
        } else {
          base[y][x] = 1; // Normal platform
        }
      }
    }
  }

  return base;
}

